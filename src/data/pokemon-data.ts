export function buildPokemon(name: string): Pokemon {
  let species = speciesList.find(species => species.name.toLowerCase() === name.toLowerCase())
  if (!species) {
    throw new Error('Unknown species name ' + name)
  }
  return new Pokemon(species)
}

export function getSpecies(name: string): PokemonSpecies {
  const species = speciesList.find(s => s.name.toLowerCase() === name.toLowerCase())
  if (species) {
    return species
  } else {
    throw new Error('Unable to find species: ' + name)
  }
}

